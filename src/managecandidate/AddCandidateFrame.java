    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String name=jTextField1.getText();
        String age=jTextField2.getText();
        String sex="";
         if(jRadioButton1.isSelected())
             sex="Male";
         if(jRadioButton2.isSelected())
             sex="Female";
         if(jRadioButton3.isSelected())
             sex="Other";
         String nationality=jTextField9.getText();
         String qualification=jTextField3.getText();
         String worth=jTextField4.getText();
         String profession=jTextField5.getText();
         String constituency=(String)jComboBox1.getSelectedItem();
         symbolname=jTextField6.getText();
         String partyname=jTextField8.getText();
         
         if(name.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Candidate Name");
         else if(age.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Age");
         else if(sex.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Select The Sex");
         else if(nationality.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Nationality");
         else if(qualification.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Qualification");
         else if(worth.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Total Worth");
         else if(profession.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Profession");
         else if(constituency.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Select Constituency");
          else if(constituency.equals("Select"))
             JOptionPane.showMessageDialog(null, "Please Select Constituency");
         else if(symbolname.isEmpty())
             JOptionPane.showMessageDialog(null,"Please Enter Symbol Name");
         else if(path.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Select Party Symbol");
         else if(partyname.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Party Name");
         else
         {
//              File file=new File(path);
//         String apath=file.getParent();
//         String ofname=file.getName();  
//         int index=ofname.lastIndexOf('.');
//         String filename=ofname.substring(0, index);
//         String exte=ofname.substring(index+1);
//         file.renameTo(new File(apath +"\\" + symbolname + "."+exte));
         
             AddCandidateInfo aci=new AddCandidateInfo();
             if(aci.doAddCandidate(name, age, sex,nationality, qualification, worth, profession,constituency, symbolname,path, partyname))
                 JOptionPane.showMessageDialog(null, "Candidate Inserted Successfully");
             else
                 JOptionPane.showMessageDialog(null, "Error In Insertion");
         }
    }//GEN-LAST:event_jButton2ActionPerformed

