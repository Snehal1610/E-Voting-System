         JFileChooser js=new JFileChooser();
        js.showOpenDialog(js);
        File f=js.getSelectedFile();
       String  path1 = f.getAbsolutePath();
       jTextField7.setText(path1);
        symbolname = f.getName();
        path=path1;
       if(path1.endsWith("jpg") || path1.endsWith("JPG") || path1.endsWith("JPEG") || path1.endsWith("png") || path1.endsWith("PNG"))
       {
          ImageIcon icon = new ImageIcon(new ImageIcon(path1).getImage().getScaledInstance(jLabel10.getWidth(), jLabel10.getHeight(), Image.SCALE_SMOOTH));
        jLabel10.setIcon(icon); 
       }
       else
       {
           JOptionPane.showMessageDialog(null, "Please Select Image with JPG or png format only");
       }
